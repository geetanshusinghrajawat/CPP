#include<iostream>
using namespace std;

int MaKeChu(int n)
{
    for(int i=0; i<n; i++)
    {
        cout<<"Dhatt Teri maki chuuuuuuu"<<endl;
    }
    cout<<endl;

    return 0;
}

int main()
{
    int gaali;
    cout<<"Enter the number how many times you want to print gaali :";
    cin>>gaali;

    MaKeChu(gaali);

    return 0;
}